/*
Stack is a library for using LIFO stacks of objects of any type.
It has the familiar operations:

	Len(), which returns the number of elements in the stack.
	IsEmpty(), which returns true if the stack has no elements.
	Push(), which adds an element to the stack.
	Pop(), which removes and returns an element from the stack,
	Peek(), which returns the element on the top of the stack without removing it.
	Clear(), which removes all elements from the stack.

Pop() and Peek() fail when called on an empty stack.
*/
package stack
